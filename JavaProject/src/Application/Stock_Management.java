    private void loadData(){
        DefaultTableModel model = new DefaultTableModel(new String[]{"id","name","category","price","quantity","path"},0);
        Table.setModel(model);
        try {
                Connection con = MySqlConnection.getConnection();
                Statement stm = con.createStatement();
                String query = "SELECT * FROM products";
                ResultSet rs = stm.executeQuery(query);
                String i,n,c,p,q, pt;
                while(rs.next()){
                    i = rs.getString("id");
                    n = rs.getString("name");
                    c = rs.getString("category");
                    p = rs.getString("price");
                    q = rs.getString("quantity");
                    pt = rs.getString("path");
                    model.addRow(new Object[]{i,n,c,p,q, pt});
                    System.out.println(n);
                }     
                if(!rs.first()){
                    JOptionPane.showMessageDialog(null,"No products exist");
                }
                
            } catch (Exception e) {
                e.printStackTrace();
                System.out.println("Failed to connect to the database");
            }
    }
    
    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed
        String nam = name.getText();
        String categor = (String) category.getSelectedItem();
        String pric= price.getText();
        String quantit= quantity.getText();
        String pat= path.getText();
        
        if (nam.isEmpty() || categor.isEmpty() || pric.isEmpty() || quantit.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please fill in all fields.");
        }
        else {
            try {     
                Connection con = MySqlConnection.getConnection();
                Statement stm = con.createStatement();
                String insertQuery = "INSERT INTO products (name, price, category, quantity, path) VALUES ('" + nam + "', '" + pric + "', '" + categor + "','" + quantit + "','" + pat + "' )";
                int rowsAffected = stm.executeUpdate(insertQuery);     
                if (rowsAffected <= 0) {
                    JOptionPane.showMessageDialog(rootPane, "Failed to conduct the operation!");
                } 
            } catch (Exception e) {
                System.out.println("Database error: " + e.getMessage());
            }

        }
        loadData();
        
        
    }//GEN-LAST:event_jToggleButton3ActionPerformed

