    private void jToggleButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton4ActionPerformed
        int[] selectedRows = Table.getSelectedRows();
    if (selectedRows.length > 0) {
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        for (int i = selectedRows.length - 1; i >= 0; i--){
            int modelRowIndex = Table.convertRowIndexToModel(selectedRows[i]);
            int id = Integer.parseInt(model.getValueAt(modelRowIndex, 0).toString()); // Corrected line
            model.removeRow(modelRowIndex);

            // Delete the row from the database using the obtained ID
            try {     
                Connection con = MySqlConnection.getConnection();
                Statement stm = con.createStatement();
                String deleteQuery = "DELETE FROM products WHERE id = " + id;
                int rowsAffected = stm.executeUpdate(deleteQuery);     
                if (rowsAffected <= 0) {
                    JOptionPane.showMessageDialog(rootPane, "Failed to delete row from database!");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Row deleted from database successfully!");
                }
            } catch (Exception e) {
                System.out.println("Database error: " + e.getMessage());
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "No rows selected.");
    }
    }//GEN-LAST:event_jToggleButton4ActionPerformed

